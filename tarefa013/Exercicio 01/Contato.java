public class Contato {
    private String nome;
    private String email;
    
    
    /** 
     * @return String
     */
    public String getNome() {
        return nome;
    }
    
    /** 
     * @param nome
     */
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    /** 
     * @return String
     */
    public String getEmail() {
        return email;
    }
    
    /** 
     * @param email
     */
    public void setEmail(String email) {
        this.email = email;
    }
}